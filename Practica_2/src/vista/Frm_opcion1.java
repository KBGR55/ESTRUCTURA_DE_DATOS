    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (txtBoolean.getText().trim().isEmpty() || txtChar.getText().trim().isEmpty() || txtDouble.getText().trim().isEmpty() || txtFloat.getText().trim().isEmpty() || txtLong.getText().trim().isEmpty()|| txtString.getText().trim().isEmpty() || String.valueOf(jsnBye.getValue()).trim().isEmpty() || String.valueOf(jsnInt.getValue()).trim().isEmpty() || String.valueOf(jsnShort.getValue()).trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campos vacios", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            if (cp.llenarDatos(txtString.getText(),txtBoolean.getText(),txtChar.getText().charAt(0),jsnBye.getValue().toString(),jsnShort.getValue().toString(),jsnInt.getValue().toString(),txtLong.getText(),txtFloat.getText(),txtDouble.getText())) {
                JOptionPane.showMessageDialog(null, "Se han ingresado correctamente", "OK", JOptionPane.INFORMATION_MESSAGE);
                cp.medirDatosLLenos();
                cargarTabla();
            } else {
                JOptionPane.showMessageDialog(null, "Ingresar correctamente los campos", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

