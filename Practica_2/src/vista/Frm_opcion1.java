    private void cargarTabla() {
        tdl_datos.updateUI();
        DefaultTableModel tablaDefa = new DefaultTableModel();
        tablaDefa.addColumn("Tipo de Dato");
        tablaDefa.addColumn("Bits de los datos vacios");
        tablaDefa.addColumn("Bits de los datos llenos");
        tablaDefa.addColumn("Valores de los datos"); 
        tdl_datos.setModel(tablaDefa);
        String[] datos = new String[4];
        for (int i = 0; i < cp.getM().length; i++) {
            datos[0] = String.valueOf(cp.getM()[i].getMedir().getTipoDato());
            datos[1] = String.valueOf(cp.getM()[i].getMedir().getVacio());
            datos[2] = String.valueOf(cp.getM()[i].getMedir().getLleno());
            datos[3] = valores(i);
            tablaDefa.addRow(datos);
        }
        tdl_datos.setModel(tablaDefa);
        //No pueda editarse la tabla
        tdl_datos.setEnabled(false);
        
    }
     
    public String valores(int l){
        switch (l) {
            case 0:
                return cp.getDatos().getDatoString();
            case 1:
                return String.valueOf(cp.getDatos().getDatoBoolean());
            case 2:
                return String.valueOf(cp.getDatos().getDatoByte());
            case 3:
                return String.valueOf(cp.getDatos().getDatoChar());
            case 4:
                return String.valueOf(cp.getDatos().getDatoDouble());
            case 5:
                return String.valueOf(cp.getDatos().getDatoFloat());
             case 6:
                return String.valueOf(cp.getDatos().getDatoLong());
            case 7:
                return String.valueOf(cp.getDatos().getDatoShort());
            case 8:
                return String.valueOf(cp.getDatos().getDatoInt());
            default:
                throw new AssertionError();
        }
    }
     
